# title() : 以首字母大写的方式显示每个单词，即将每个单词的首字母全部大写或者全部小写。

# 更改字符串的小写
name_big = "ada love"
print(name_big.title())  # 输出: Ada Love

# 更改字符串的大写
name_small = "All The World"
print(name_small.title())  # 输出: All The World
