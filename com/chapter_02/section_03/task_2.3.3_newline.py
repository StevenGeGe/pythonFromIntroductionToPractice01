# 换行符
# 用换行符或者制表符代替空白
# \t : 制表符，一般四个空格
# \n : 换行符

# 正常
print("正常:nihao python")
# 加了制表符
print("加了制表符:\tnihao python")

# 正常
print("正常:nihao python, nihao yong")
# 加了换行符
print("加了换行符:\n nihao\n python,\n nihao\n yong\n")

