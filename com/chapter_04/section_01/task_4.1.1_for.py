# for循环打印列表元素
# 获取列表中的第一个元素(alice)，然后存储到变量中(magician)
# 对于用于存储列表中每个元素的临时变量， 可以指定任何名称

magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(magician)

cats = ['xiaoju', 'jiafei']
dogs = ['erha', 'jinmao']

for cat in cats:
    print(cat)
for dog in dogs:
    print(dog)
